                <h3>Entry Formalities</h3>
                <p><strong>Foreigner Nationals</strong> <br />
                      All foreign nationals can stay in the islands for 30 days. This can be extended by another 15 days with permission. They require a permit to stay from the immigration authorities. In addition, permits can also be obtained from: Indian Missions Overseas, Foreigner Registration Offices at Delhi, Mumbai, Chennai, Calcutta and the immigration authorities at the airports at Delhi, Mumbai, Calcutta and Chennai.</p>
                <p><strong>For Indians</strong> <br />
                      Indians need no permit to visit Andamans Islands. But permits are required to visit Nicobar Islands and other tribal areas, which are given in exceptional cases. Application on a prescribed form may be addressed to the Deputy Commissioner, Andaman District, Port Blair.</p>
                <h3><a name="tips" id="tips"></a>Tips for Visitors</h3>
                <br />
                <strong>Do's</strong>
                <ul class="tick1">
                      <li>All visitors please confirmed the air tickets and accommodation in advance before proceeding to the Andaman Island to avoid accommodation problems.</li>
                      <li>Foreign nationals should obtain the required permit from the immigration authorities soon after landing on the island at Airport/Harbour.</li>
                      <li>Visit the permitted areas in the Islands only.</li>
                      <li>Please carry legal documents like permit, passport for foreign nationals and any valid ID cards for Indians for identity.</li>
                      <li>Swim in safe area only and consult life guards before entering sea.</li>
                      <li>Keep the beaches and the environment clean. Please avoid using Polybags and use jute/cotton &amp; paper bags.</li>
                      <li>Dispose of the garbage at proper places/dustbins and avoid plastic.</li>
                      <li>Observe decency and decorum in dress.</li>
                      <li>Avail the service of Scuba Dive instructors having certification of international/professional organizations like PADI, CMAS, NAUI, BSAC and SSI for safe diving experience.</li>
                      <li>Please follow the rules &amp; policy while passing through Tribal areas.</li>
                      <li>On beach, parents are requested to keep an eye on your kids at all times.</li>
                      <li>Please drink sufficient water to avoid dehydration.</li>
                      <li>Please pick up your bags and bottle while leaving the visiting spot.</li>
                    </ul>
                <br />
                <strong>Dont's</strong>
                <ul class="tick1">
                      <li>Foreign nationals should not enter the islands without required permit.</li>
                      <li>Do not enter into restricted/tribal areas.</li>
                      <li>Do not take video, film or photographs inside Tribal Reserve areas of the indigenous tribes.</li>
                      <li>Do not carry sea fans and seashells unless specific permits are obtained from Fisheries Department.</li>
                      <li>Do not light fire in forest areas.</li>
                      <li>Do not stay on the beaches or forest during nights by putting up down tents/hammocks.</li>
                      <li>Nudity on beaches and public places is forbidden.</li>
                      <li>Do not enter the National Parks without permission.</li>
                    </ul>
                <h3>Flight Charter</h3>
                <strong>Port Blair Open For Charter Flights</strong>
                <p>Ministry of Civil Aviation, Govt. of India has permitted foreign charter flights to land at Port Blair subject to the fulfillment of the guidelines issued by Director General, Civil Aviation, Govt. of India. These undulating islands in the Bay of Bengal are covered with dense forests and endless variety of exotic flora and fauna. The topography of the islands is hilly and abounds in evergreen forests. The sandy beaches on the edge of meandering coastline are fringed with coconut palms that sway to the rhythm of the sea. The sea around the islands offers excellent scope for adventure water sports. The rare flora and fauna, underwater marine life and corals, crystal clear water and mangrove-lined creeks, offer a dream-view of the rare gifts of nature. This blessed Islands attract any nature-lover, who seeks absolute peace and tranquility in the lap of Mother Nature. Adventure tourism like trekking, island camping, snorkeling, scuba diving etc., are the major attractions here. A visit to these islands is a memorable lifetime experience. Andaman Islands are a world class eco-tourism destination and has immense potential for adventure water sports.</p>
