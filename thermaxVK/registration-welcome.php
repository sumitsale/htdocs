Philosophy: 
At the centre of Thermax Group's Corporate Philosophy are the Core Values that Thermax nurtures - Honesty & Integrity, Commitment, Respect and Concern for the Environment and Community.  Thermax is committed to carrying out its business in ethical and sustainable manner and expects all its supply chain partners to follow minimum standards of Honesty & Integrity, Commitment, Respect and Concern for the Environment and Community. 

Purpose of this document:
The Purpose of this document is to define the minimum standards that all of Thermax's supply chain partners are required to follow. The supply chain partners (hereinafter collectively called Vendors) include, but are not limited to:
1.	Vendors, suppliers
2.	Sub-contractors, 
3.	Labour contractors,
4.	Site contractors
5.	Engineering sub-contractors

Each vendor is required to act responsibly and with integrity and honesty, and to comply with this code and its underlying principles, policies and instructions. By signing on a copy of this Code of Conduct, the respective Vendor commits itself to total compliance with this Code of Conduct in letter & spirit

Compliance with applicable laws/regulations/government orders
All business and other activities of Thermax shall be carried out strictly in compliance with all applicable laws/regulations/government orders and under the principles of good corporate citizenship in each country where Thermax is present. 

It is mandatory for each vendor to comply with the requirements of all those laws and regulations that apply to the Vendor's workmen, staff, premises, business practices and to its relationship with Thermax. 

Respect for human and labour rights
Thermax supports and respects the protection of human rights & labour rights as defined in the relevant laws of the country in which it is operating. Every vendor is required to abide by all laws, rules, regulations & principles as applicable to its country of operation concerning human & labour rights.

Thermax does not accept any form of forced or compulsory labour, or the use of child labour. All vendors are required to follow these principles in their business.


