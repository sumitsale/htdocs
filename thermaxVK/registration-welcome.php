Occupational health and safety
Thermax endeavours to create hazard-free workplaces in all its locations for its employees, vendors, contractors, and others working in Thermax locations by applying high standards of occupational health and safety. Thermax strives to assure the safety of its products and solutions through its product and solution development processes.

Each vendor is responsible for complying with the safety instructions, for using personal protection equipment when required, and for reporting on any shortcomings regarding safety instructions or protection measures while they are in Thermax premises or Thermax controlled sites. Thermax also encourages all its vendors to follow sound Occupational health and safety in their own premises.

Bribery & corrupt business practices
The Vendor will not, directly or through any other person or firm, offer, promise or give to any of Thermax Group's employees or to any third person any material or other benefit which he/she is not legally entitled to, in order 	to obtain in exchange any advantage of any kind whatsoever during the dealings with Thermax.

The Vendor will not enter into any undisclosed agreement or understanding, whether formal or informal regarding prices, specifications, certifications, any other matter that restricts healthy competitive environment or introduces cartelization when dealing with Thermax. 

The Vendor will not commit any offence under the relevant Laws as applicable to the particular location; further the Vendor will not use improperly, for purposes  of  competition  or  personal  gain,  or pass  on  to  others,  any information or document provided by Thermax as part of the business relationship,  regarding  plans,  technical  proposals  and  business  details, including information contained or transmitted electronically. 

Conflict of Interest
In case of any close relative (blood relatives, in-laws, cousins etc.) of any key officials of the Vendor working in Thermax, the Vendor is required to promptly disclose the same to Vice President - Corporate Sourcing Group who shall make sure that the concerned close relative is not directly involved in any dealings with that vendor.

Innovation and protection of proprietary information
Thermax supports and encourages innovation by its vendors in all areas of its activities.

Thermax's intellectual property is one of its most valuable assets and the patents, trademarks, copyrights, trade secrets, and other proprietary information of Thermax must be protected. Each Vendor is required to keep confidential all information and data that is disclosed to it by Thermax. 

The Vendor is required to sign a separate Non-Disclosure Agreement to secure the information disclosed by Thermax to the vendor.

